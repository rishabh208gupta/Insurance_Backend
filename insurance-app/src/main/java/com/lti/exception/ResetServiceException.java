package com.lti.exception;

public class ResetServiceException extends Exception {

	public ResetServiceException() {
		super();
	}

	public ResetServiceException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public ResetServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResetServiceException(String message) {
		super(message);
	}

	public ResetServiceException(Throwable cause) {
		super(cause);
	}

}
